            The school was established in 1983 during the term of Hon. Eufrasio Mascarinas as mayor of the town. <br>
Bohol's prominent educators together with the local government unit had joined hands to build a community college for local residents who are economically challenged to send their children to big cities to obtain college education. <br>
During that time, Tagbilaran City was the only nearest place that offers college education and only few are capable of sending their children to Cebu and Manila for higher education. <br>

It is through the efforts of then Sangguniang Bayan secretary, Cesar Mascarinas, parish priest Msgr. Camilo V. Auza and an educator Lourdes H. Torrefranca, Ph.D., <br>
that drafted the idea of funding a college into a reality. Finally, Resolution no. 78 (series of 1983) of the town council was passed for this act. <br>
